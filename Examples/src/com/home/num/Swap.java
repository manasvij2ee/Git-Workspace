package com.home.num;

public class Swap {
	public static void main(String[] args) {
		int x = 25, y = 50, temp;
		System.out.println("x=" + x);
		System.out.println("y=" + y);
		temp = x;
		x = y;
		y = temp;
		System.out.println(" After Swapping value of x= " + x);
		System.out.println(" After Swapping value of y= " + y);

	}
}
